export default function BottomNav() {
  const pathname = usePathname();

  // admin 경로에서는 렌더링하지 않음 (admin 전용 레이아웃 사용)
  if (pathname.startsWith("/admin")) return null;
  // 온보딩·로그인 페이지에서는 숨김
  if (pathname.startsWith("/onboarding") || pathname === "/login") return null;

  function isActive(href: string) {
    if (href === "/") return pathname === "/";
    return pathname.startsWith(href);
  }

  return (
    <nav
      className="fixed bottom-0 left-0 right-0 z-40 bg-white border-t border-gray-200 safe-area-pb"
      aria-label="메인 네비게이션"
    >
      <div className="max-w-md mx-auto flex">
        {NAV_ITEMS.map(({ href, label, icon }) => {
          const active = isActive(href);
          return (
            <Link
              key={href}
              href={href}
              aria-label={label}
              aria-current={active ? "page" : undefined}
              className={`flex-1 flex flex-col items-center justify-center gap-0.5 py-2 min-h-[56px] transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-teal-500 focus-visible:ring-inset ${
                active ? "text-teal-600" : "text-gray-400 hover:text-gray-600"
              }`}
            >
              {icon(active)}
              <span
                className={`text-[10px] font-medium leading-none ${
                  active ? "text-teal-600" : "text-gray-400"
                }`}
              >
                {label}
              </span>
              {active && (
                <span className="sr-only">(현재 페이지)</span>
              )}
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
